package connexion;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.Scanner;

/**
 *
 * @author kasmi
 */
public final class connexion {
    public Connection conn;
    private Statement statement;
    public static connexion db;
    private connexion() {
        String url= "jdbc:mysql://localhost/";
        String dbName = "aukh";
        String driver = "com.mysql.jdbc.Driver";
        String userName = "root";
        String password = "";
        try {
            Class.forName(driver).newInstance();
            this.conn = (Connection)DriverManager.getConnection(url+dbName,userName,password);
        }
        catch (Exception sqle) {
            sqle.printStackTrace();
        }
    }
    /**
     *
     * @return MysqlConnect Database connection object
     */
    public static synchronized connexion getDbCon() {
        if ( db == null ) {
            db = new connexion();
        }
        return db;
 
    }
    /**
     *
     * @param query String The query to be executed
     * @return a ResultSet object containing the results or null if not available
     * @throws SQLException
     */
    public ResultSet query(String query) throws SQLException{
        statement = db.conn.createStatement();
        ResultSet res = statement.executeQuery(query);
        return res;
    }
    public boolean login(String u,String p) throws SQLException
    {
        PreparedStatement ps = conn.prepareStatement("select * from employe where login= ? and password = ? ");
        ps.setString(1, u);
        ps.setString(2, p);
        ResultSet r = ps.executeQuery();
        if (r.next()) {
            return true;
        }return false;
    }
    /**
     * @desc Method to insert data to a table
     * @param insertQuery String The Insert query
     * @return boolean
     * @throws SQLException
     */
    public int insert(String insertQuery) throws SQLException {
        statement = db.conn.createStatement();
        int result = statement.executeUpdate(insertQuery);
        return result;
 
    }
   
}

